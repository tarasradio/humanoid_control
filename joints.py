class LegJoints:
  def __init__(self):
    self.hip_yaw = 0.0 # z (vertical) axis (bottom directed)
    self.hip_roll = 0.0 # x (Longitudinal) axis (forward directed)
    self.hip_pitch = 0.0 # y (Lateral) axis (right directed)
    self.knee_pitch = 0.0 # y (Lateral) axis
    self.ankle_roll = 0.0 # x (Longitudinal) axis
    self.ankle_pitch = 0.0 # y (Lateral) axis